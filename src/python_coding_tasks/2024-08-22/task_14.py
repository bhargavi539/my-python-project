#Loop
"""
for variable in range(start,stop,step)
// code that need to be executed multiple times
"""

for var_name in range(1, 11, 2):  #start,stop-1,step
    print(var_name, end=" ")  #1, 3, 5, 7, 9

print("\n")

for i in range(2, 11, 2):
    print(i, end =' ')  #2, 4, 6, 8, 10

print("\n")

for number in range(10,0,-2):
    print(number)

print("\n")

for doubt in range(0,-10,-2):
    print(doubt)

